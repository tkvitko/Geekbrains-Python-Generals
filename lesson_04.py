# Задание 3
# Дан список заполненный произвольными целыми числами.
# Получите новый список, элементами которого будут только уникальные элементы исходного.

my_list = [2, 2, 5, 12, 8, 2, 12]
result_list = [2, 2, 5, 12, 8, 2, 12]

# Получаем список дубликатов и записываем его в my_list
for element in my_list:
    my_list.remove(element)

# Удаляем из результирующего списка все дубликаты
for element in my_list:
    while element in result_list:
        result_list.remove(element)

print(result_list)

