my_list_2 = [2, 7, 12, 3]

# для каждого элемента из списка 2
for element in my_list_2:
# до тех пор, пока такой же находится в списке 1
    while element in my_list_1:
# удаляем этот элемент из списка 1
        my_list_1.remove(element)
# выведем итоговый список 1
print(my_list_1)


